            только 2 — годен или нет. Здесь же потребитель сам определяет, удовлетворяют ли характеристики СИ его
            потребностям. Допустим, вам откалибровали концевую меру длины, и полученное значение отклоняется от номинала
            больше, чем это разрешено ГОСТом. По правилам поверки — это брак. Но зная теперь действительный размер меры,
            вы можете продолжать ей пользоваться, просто производя при измерениях корректировку показаний на величину
            отклонения. Или при калибровке оказалось, что погрешность индикатора ИЧ-10 не 0,020 мм, как предписывается
            ГОСТом, а 0,025 мм. Но если вы измеряете размеры в основном с точностью «до десятки» (0,1 мм), то данное
            расхождение не критично, и можно вполне продолжать пользоваться инструментом, а не покупать новый.
          </p>
