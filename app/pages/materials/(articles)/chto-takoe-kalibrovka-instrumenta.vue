            Во-первых, это проще. Организаций, которые оказывают услуги по калибровке больше, чем аналогичных
            поверителей. Сейчас многие небольшие лаборатории аккредитуются в РСК (Российской Системе Калибровки), и
            имеют право выписывать официальные сертификаты, признаваемые даже в суде и других официальных учреждениях.
